function [CSX] = defineFieldDumps(CSX, mesh, layer_list, UC);
  td_dumps = UC.td_dumps;
  fd_dumps = UC.fd_dumps;
  layer_td = UC.layer_td;
  layer_fd = UC.layer_fd;
  xMin = -UC.lx/2;
  xMax = -xMin;
  yMin = -UC.ly/2;
  yMax = -yMin;
  zMin = mesh.z(1);
  zMax = mesh.z(end);
  if layer_td || layer_fd;
    z_xy = get_xy_center_z(layer_list);
    for i = 1:size(z_xy,2);
      zval = z_xy(i);
      if layer_td;
        name = strcat('t_dump_xy', num2str(i));
        CSX = AddDump(CSX, name);
        CSX = AddBox(CSX, name, 1, [xMin, yMin, zval], [xMax, yMax, zval]);
      end;
      if layer_fd;
        name = ['f_dump_xy' num2str(i)];
        try;
          freq = UC.dump_frequencies;
          CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
          CSX = AddBox(CSX, name, 1, [xMin, yMin, zval], [xMax, yMax, zval]);
        catch lasterror;
        end;
      end;      
    end;    
  end;
  if fd_dumps;
    try;
      freq = layer_list{1}{2}.dump_frequencies;
      name = 'Ef_xz';
      CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
      CSX = AddBox(CSX, name, 1, [xMin, 0, zMin], [xMax, 0, zMax]);
      name = 'Ef_yz';
      CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
      CSX = AddBox(CSX, name, 1, [0, yMin, zMin], [0, yMax, zMax]);    
    catch lasterror;
    end;
  end;
  if td_dumps;  
    name = 'Et_xz';
    CSX = AddDump(CSX, name);
    CSX = AddBox(CSX, name, 1, [xMin, 0, zMin], [xMax, 0, zMax]);
    name = 'Et_yz';
    CSX = AddDump(CSX, name);
    CSX = AddBox(CSX, name, 1, [0, yMin, zMin], [0, yMax, zMax]);    
    name = 'Ht_xz';
    CSX = AddDump(CSX, name, 'DumpType', 1);
    CSX = AddBox(CSX, name, 1, [xMin, 0, zMin], [xMax, 0, zMax]);
    name = 'Ht_yz';
    CSX = AddDump(CSX, name, 'DumpType', 1);
    CSX = AddBox(CSX, name, 1, [0, yMin, zMin], [0, yMax, zMax]);   
  end;
end