function [CSX] = defineCalculations(CSX, mesh, layer_list, UC, port);
  td_dumps = UC.td_dumps;
  fd_dumps = UC.fd_dumps;
  layer_td = UC.layer_td;
  layer_fd = UC.layer_fd;
  xMin = -UC.lx/2;
  xMax = -xMin;
  yMin = -UC.ly/2;
  yMax = -yMin;
  zMin = -UC.lz/2;
  zMax = -zMin;
  if layer_td || layer_fd;
    z_xy = get_xy_center_z(layer_list);
    for i = 1:size(z_xy)(2);
      zval = z_xy(i);
      if layer_td;
        name = ["t_dump_xy" num2str(i, "%i")];
        CSX = AddDump(CSX, name);
        CSX = AddBox(CSX, name, 1, [xMin, yMin, zval], [xMax, yMax, zval]);
      endif;
      if layer_fd;
        name = ["f_dump_xy" num2str(i, "%i")];
        try;
          freq = UC.dump_frequencies;
          CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
          CSX = AddBox(CSX, name, 1, [xMin, yMin, zval], [xMax, yMax, zval]);
        catch lasterror;
        end_try_catch;
      endif;      
    endfor;    
  endif;
  if fd_dumps;
    try;
      freq = layer_list{1}{2}.dump_frequencies;
      name = "Ef_xz";
      CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
      CSX = AddBox(CSX, name, 1, [xMin, 0, zMin], [xMax, 0, zMax]);
      name = "Ef_yz";
      CSX = AddDump(CSX, name, 'DumpType', 10, 'Frequency', freq);
      CSX = AddBox(CSX, name, 1, [0, yMin, zMin], [0, yMax, zMax]);    
    catch lasterror;
    end_try_catch;
  endif;
  if td_dumps;  
    name = "Et_xz";
    CSX = AddDump(CSX, name);
    CSX = AddBox(CSX, name, 1, [xMin, 0, zMin], [xMax, 0, zMax]);
    name = "Et_yz";
    CSX = AddDump(CSX, name);
    CSX = AddBox(CSX, name, 1, [0, yMin, zMin], [0, yMax, zMax]);    
  end;
  if UC.s_dumps;
    try;
      dumpS11(port, UC);
    catch
      msg = lasterror.message;
      display(["Fehler: " msg]);
    end_try_catch;
  endif;
endfunction;